import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,Tender,apiUrlTender} from '../../../constants/defaultValues';
import {authHeader} from '../../../helpers/authheader';
import axios from 'axios';
export const tenderactivityService = {
  fetchtenderactivity,
  createtenderactivity,
  updatetenderactivity,
  deletetenderactivity,
  fileUpload,
};
  export function fetchtenderactivity() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlTender}${Tender}activity`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createtenderactivity(name,is_subactivity,status) {
  const user_id = parseInt(localStorage.getItem('user_id'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,is_subactivity,user_id,status})
};
return fetch(  `${apiUrlTender}${Tender}activity/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
