import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,MIS} from '../../../constants/defaultValues';
import {authHeader} from '../../../helpers/authheader';
import axios from 'axios';

export const misqscmasterService = {
  fetchmisqscmaster,
  createmisqscmaster,
  updatemisqscmaster,
  deletemisqscmaster,
  fileUpload
};
  export function fetchmisqscmaster() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrl}${MIS}misqscdetails`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createmisqscmaster(reportname,timetaken,status) {
  const user_id = parseInt(localStorage.getItem('user_id'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ reportname,timetaken,user_id,status})
};
return fetch(  `${apiUrl}${MIS}misqscdetails/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
