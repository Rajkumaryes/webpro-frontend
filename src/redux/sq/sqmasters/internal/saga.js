import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,SQ} from '../../../../constants/defaultValues';
import {authHeader} from '../../../../helpers/authheader';
import axios from 'axios';

export const InternalService = {
  fetchinternal,
  createinternal,
  updateinternal,
  deleteinternal,
  fileUpload
};
  export function fetchinternal() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrl}${SQ}internal`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createinternal(name,status) {
  const user_id = parseInt(localStorage.getItem('user_id'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,user_id,status})
};
return fetch(  `${apiUrl}${SQ}internal/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
