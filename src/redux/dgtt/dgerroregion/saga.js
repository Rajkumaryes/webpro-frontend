import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,DG,apiUrlDG} from '../../../constants/defaultValues';
import {authHeader} from '../../../helpers/authheader';
import axios from 'axios';

export const RegionService = {
  fetchregion,
  createregion,
  updateregion,
  deleteregion,
  fileUpload
};
  export function fetchregion() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlDG}${DG}dgerrorregion`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createregion(name,status) {
  const user_id = parseInt(localStorage.getItem('user_id'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,user_id,status})
};
return fetch(  `${apiUrlDG}${DG}dgerrorregion/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
