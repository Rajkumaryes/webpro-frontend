import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrlMainline,Mainline} from '../../../../constants/defaultValues';
import {authHeader} from '../../../../helpers/authheader';
import axios from 'axios';

export const requestService = {
  fetchrequest,
  createrequest,
  updaterequest,
  deleterequest,
  fileUpload
};
  export function fetchrequest() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlMainline}${Mainline}request`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createrequest(name,tat_limit,status) {
  const user_id = parseInt(localStorage.getItem('username'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,tat_limit,user_id,status})
};
return fetch(  `${apiUrlMainline}${Mainline}request/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
