import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,FeedersSchedules,apiUrlFeeder} from '../../../../constants/defaultValues';
import {authHeader} from '../../../../helpers/authheader';
import axios from 'axios';

export const ActivityService = {
  fetchactivity,
  createactivity,
  updateactivity,
  deleteactivity,
  fileUpload
};
  export function fetchactivity() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlFeeder}${FeedersSchedules}activitytype`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createactivity(name,status) {
  const user_id = parseInt(localStorage.getItem('username'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,user_id,status})
};
return fetch(  `${apiUrlFeeder}${FeedersSchedules}activitytype/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
