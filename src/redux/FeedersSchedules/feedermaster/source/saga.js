import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,FeedersSchedules,apiUrlFeeder} from '../../../../constants/defaultValues';
import {authHeader} from '../../../../helpers/authheader';
import axios from 'axios';

export const sourceService = {
  fetchsource,
  createsource,
  updatesource,
  deletesource,
  fileUpload
};
  export function fetchsource() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlFeeder}${FeedersSchedules}source`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createsource(name,status) {
  const user_id = parseInt(localStorage.getItem('username'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,user_id,status})
};
return fetch(  `${apiUrlFeeder}${FeedersSchedules}source/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
