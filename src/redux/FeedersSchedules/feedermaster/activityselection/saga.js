export function fileUpload(file)
{
  let data = new FormData();
  data.append('formFile', file);
  var url = `${apiUrlFeeder}${FeedersSchedules}activityselection/bulkupload`
  return axios.post(url, data, {
    headers: {
    'content-type': 'multipart/form-data',
    ...authHeader()
    }
    })
    .then(res => {
     return res
      
    })
    .catch()
         
}
  export default function* rootSaga() {
    yield all([
      fork(fetchactivityselection),
      fork(createactivityselection),
     fork(updateactivityselection),
     fork(deleteactivityselection),
  
    ]);
  }
  