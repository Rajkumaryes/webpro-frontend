import { all, call, fork, put, takeEvery } from 'redux-saga/effects';
import {apiUrl,apiUrlFeeder,FeedersSchedules} from '../../../../constants/defaultValues';
import {authHeader} from '../../../../helpers/authheader';
import axios from 'axios';

export const ActivityselectionService = {
  fetchactivityselection,
  createactivityselection,
  updateactivityselection,
  deleteactivityselection,
  fileUpload
};
  export function fetchactivityselection() {

    const requestOptions = {
      method: 'POST',
      headers: {
          'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
          'Content-Type': 'application/json',
          ...authHeader()
        },
  };
  return fetch(  `${apiUrlFeeder}${FeedersSchedules}activityselection`, requestOptions)
  .then(response => response.json())
  .then(user => { 
      return user;
      }) 
      .catch((error) => {
    }); 

}  
export function createactivityselection(name,status) {
  const user_id = parseInt(localStorage.getItem('username'))

  const requestOptions = {
    method: 'POST',
    headers: {
        'Accept': 'application/json, text/plain, */*',  // It can be used to overcome cors errors
        'Content-Type': 'application/json',
        ...authHeader()
      },
    body: JSON.stringify({ name,user_id,status})
};
return fetch(  `${apiUrlFeeder}${FeedersSchedules}activityselection/create`, requestOptions)
.then(response => response.json())
.then(user => { 
    return user;
    }) 
    .catch((error) => {
  }); 

}
  
